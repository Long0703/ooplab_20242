import java.util.Scanner;
public class Bai225 {

	public static void main(String[] args) {
		double a;
		double b;
		Scanner sc= new Scanner(System.in);
		a=sc.nextDouble();
		b=sc.nextDouble();
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		sc.close();
	}

}
